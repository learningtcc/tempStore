package com.ks.model.swaparena;
/**
 * 交换排名竞技赛购买次数配置
 * @author hanjie.l
 *
 */
public class SwapArenaBuySetting {
	
	/**
	 * 次数
	 */
	private int times;
	
	/**
	 * 话费混钻
	 */
	private int cost;

	public int getTimes() {
		return times;
	}

	public void setTimes(int times) {
		this.times = times;
	}

	public int getCost() {
		return cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}
}
